
/**
 * The RunProgram calls the HexoGame class, starting the game.
 * 
 * @author Chris Lindgren
 * @version 1.0
 */
public class RunProgram
{
    /**
     * The main method starts the card game program.
     * 
     */
    public static void main()
    {
        HexoGame start = new HexoGame();
        start.runHexo();
    }
}
